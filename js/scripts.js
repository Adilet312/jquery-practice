$(document).ready(function () {

  $(".text-click").click(function() {
    $("#flower").toggle();
    $('.text-click').toggle();
    $('.text-click-off').toggle();
  });

  $(".text-click-off").click(function() {
    $("#flower").toggle();
    $('.text-click').toggle();
    $('.text-click-off').toggle();
  });




});
